package Intervieew;

public class SwapString {
    public static void main(String[] args) {
        String s1="prasanta";
        String s2="kumar";
        String temp;
        temp=s1;//prasanta
        s1=s2;//kumar
        s2=temp;//prasanta
        System.out.println(s1);
        System.out.println(s2);

    }
}
